def render(text, fill="#", scale=1, spacing=1):
    rows = ["" for _ in range(7 * scale)]
    for ch in text.upper():
        glyph = FONT.get(ch, FONT[" "])
        for r, line in enumerate(glyph):
            cell = "".join((fill if c == "#" else " ") * scale for c in line)
            for s in range(scale):
                rows[r * scale + s] += cell + " " * spacing
    return "\n".join(rows)


def main():
    parser = argparse.ArgumentParser(description=__doc__)
    parser.add_argument("text", help="Text to render (letters, digits, basic punctuation)")
    parser.add_argument("--char", default="#", help="Fill character (default: #)")
    parser.add_argument("--scale", type=int, default=1, help="Pixel scale factor (default: 1)")
    parser.add_argument("--spacing", type=int, default=1, help="Columns of space between letters")
    args = parser.parse_args()
    print(render(args.text, fill=args.char, scale=args.scale, spacing=args.spacing))


if __name__ == "__main__":
    main()
